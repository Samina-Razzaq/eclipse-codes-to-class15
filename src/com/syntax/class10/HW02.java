public class HW02 {

	public static void main(String[] args) {
	
int[][] numbers= {
		
		{10, 20, 30,40},
		{5, 15, 25, 35}	,
		{3, 4, 5, 6}
};
		int sum=0;
		for(int i=0; i<numbers.length; i++) {
			for(int j=0; j<numbers[i].length; j++) {
				sum+= numbers[i][j];
				
			}}
			
			System.out.println("The sum of all the numbers is "+sum);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
